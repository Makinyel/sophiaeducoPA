    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        PeriodosController pc = new PeriodosController();
        Periodos periodos = new Periodos();
        periodos.setFechaInicio(getFecha(fechaInicio));
        periodos.setFechaFin(getFecha(fechaFinal));

        try {
            if (pc.addPeriodo(periodos)) {
                JOptionPane.showMessageDialog(null, "El periodo se agrego con exito.");
                getData();
                jTabbedPane1.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, "El periodo no se pudo agregar.");

            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
        System.out.println(getFecha(fechaFinal));
    }//GEN-LAST:event_btnGuardarMouseClicked

