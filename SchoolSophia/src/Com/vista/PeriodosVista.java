    public void getData() {
        DefaultTableModel dtm = (DefaultTableModel) tablaPeriodos.getModel();
        dtm.setRowCount(0);
        pc.getAllPeriodos().forEach((u) -> dtm.addRow(new Object[]{u.getFechaInicio(), u.getFechaFin()}));
    }

