    private void cmbFiltroItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbFiltroItemStateChanged
        switch (cmbFiltro.getSelectedItem().toString()) {
            case "Id" ->
                txtBuscarAsignatura.setText("Escriba el id de la asignatura.");
            case "Nombre" ->
                txtBuscarAsignatura.setText("Ingrese el nombre de la asignatura.");
            case "Seleccione una opcion." ->
                txtBuscarAsignatura.setText("");
            default ->
                txtBuscarAsignatura.setText("");
        }
    }//GEN-LAST:event_cmbFiltroItemStateChanged

