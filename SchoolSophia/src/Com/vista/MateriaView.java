    private void getData(String g) {
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        mc.getAllMaterias(g).forEach((u) -> dtm.addRow(new Object[]{u.getId(), u.getNombreCurso(), u.getNombreCarrera(), u.getNivelCarrera()}));
    }

