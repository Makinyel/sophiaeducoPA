    public void updateTable(String x) {
        try {
            DefaultTableModel dfm = (DefaultTableModel) tablaCarreras.getModel();
            dfm.setRowCount(0);
            cc.getAllCarreras(x).forEach((u) -> dfm.addRow(new Object[]{u.getId(),
                u.getNombre(),
                u.getFechaCreacion(),}));
        } catch (SQLException ex) {
            Logger.getLogger(PeriodosVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

