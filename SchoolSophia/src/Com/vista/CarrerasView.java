    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        if (getIndex() == 0) {
            if (tablaCarreras.getSelectedRow() > -1) {
                String studentId = (String) tablaCarreras.getValueAt(tablaCarreras.getSelectedRow(), 1);
                if (cc.deleteByName(studentId)) {
                    JOptionPane.showMessageDialog(null, "Carrera eliminada con exito.");
                    updateTable("");
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar la a eliminar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    public void guardar(){
            Carreras carreras = new Carreras(txtNombre.getText());
        try {
            if (txtNombre.getText().isBlank() || txtNombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "El nombre no puede estar vacío.");
            } else if (cc.existCarrera(txtNombre.getText())) {
                JOptionPane.showMessageDialog(null, "Ya existe una carrera con ese nombre.", "Bad Request", JOptionPane.ERROR_MESSAGE);
            } else {
                cc.addCarreras(carreras);
                JOptionPane.showMessageDialog(null, "Se agrego la carrera con exito.");
                txtNombre.setText("");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error:" + ex.getMessage());
        }
    }

