    private String editCarrera() {
        String nombreCarrera = "";
        if (tablaCarreras.getSelectedRow() > -1) {
            txtNombre.setText("Ingrese aquí el nombre nuevo a la carrera.");
            nombreCarrera = (String) tablaCarreras.getValueAt(tablaCarreras.getSelectedRow(), 1);
            return nombreCarrera;
        } else {
            JOptionPane.showMessageDialog(this, "Debes seleccionar la carrera a editar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            jTabbedPane1.setSelectedIndex(0);
        }
        return nombreCarrera;
    }
