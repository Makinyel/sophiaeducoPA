    public void setDataStudents() {
        AlumnoController ac = new AlumnoController();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ac.getAllStudents("").forEach((u) -> dtm.addRow(new Object[]{u.getNumeroDoc(), u.getApellidos(), u.getNombre()}));
    }

