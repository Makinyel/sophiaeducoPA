    public String getValue() {
        if (jTable1.getSelectedRow() > -1) {
            String nombreAlumno = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            return nombreAlumno;
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar a un estudiante.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }

