    public void setComboItems(String tabla, String value, JComboBox c) {
        String sql = "select * from " + tabla;
        try {
            PreparedStatement ps = conection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                c.addItem(rs.getString(value));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    public void setPeriodoItems(String tabla, String value, JComboBox c) {
        String sql = "select * from " + tabla;
        try {
            PreparedStatement ps = conection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                c.addItem(rs.getString(value) + " Hasta " + rs.getString("fechaFinal"));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    public void setPeriodoItems(String tabla, JComboBox c) {
        String sql = "select * from " + tabla;
        try {
            PreparedStatement ps = conection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                c.removeAllItems();
                c.addItem(rs.getString("fechaInicio") + " Hasta " + rs.getString("fechaFinal"));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

