    public void showMaterias(int id){
        DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
        dtm.setRowCount(0);
       dmc.getDetallesMatriculas(id).forEach((u) -> dtm.addRow(new Object[]{u.getAsignatura()}));
    
    }
    public void showDetalle(int id){
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
       mc.getMatriculaById(id).forEach((u) -> dtm.addRow(new Object[]{u.getEstudiante(),u.getCarrara(),u.getPeriodo(),u.getNiveles(),u.getFechaNow()}));
    }
    
    public void addDetalle() {
        DetalleMatriculas dm = new DetalleMatriculas();
        MatriculasController mc = new MatriculasController();

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String nombre = jTable1.getValueAt(i, 0).toString();
            dm.setAsignatura(nombre);
            dm.setIdMatricula(mc.getMaxId());
            System.out.println(dm.getAsignatura());
            mc.addNewDetalleMatricula(dm);
        }

    }

    public void asd() {

        String nombreCarrera = (String) cmbCarreras.getSelectedItem();
        String nivels = (String) cmbNiveles.getSelectedItem();
        cmbMaterias.removeAllItems();
        setAsignaturas(nombreCarrera, nivels);
    }

    public void setAsignaturas(String nombreCarrera, String nivels) {
        try {
            String sql = "select * from materias where carrera = ? and nivel = ?";

            PreparedStatement ps = conection.prepareStatement(sql);
            ps.setString(1, nombreCarrera);
            ps.setString(2, nivels);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                cmbMaterias.addItem(rs.getString("nombre"));
            }
        } catch (SQLException ex) {

        }
    }

